
package TUTORIAL_4_BOUNCING_BABIES2;
public class Baby {
    
    private int babyY = 71;
    private int babyX = 134;
    private int babyYspeed = 0;
    private int babyWidth = 25;

    public Baby() {
        
    }

    public int getBabyY() {
        return babyY;
    }

    public int getBabyX() {
        return babyX;
    }

    public void setBabyY(int babyY) {
        this.babyY = babyY;
    }

    public void setBabyX(int babyX) {
        this.babyX = babyX;
    }

    public void setBabyYspeed(int babyYspeed) {
        this.babyYspeed = babyYspeed;
    }

    public void setBabyWidth(int babyWidth) {
        this.babyWidth = babyWidth;
    }

    public int getBabyYspeed() {
        return babyYspeed;
    }

    public int getBabyWidth() {
        return babyWidth;
    }
    
    
}
